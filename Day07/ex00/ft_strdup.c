#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*str;
	int		len;

	len = 0;
	while (src[len] != '\0')
		len++;
	str = (char*)malloc(sizeof(*src) * (len + 1));
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
