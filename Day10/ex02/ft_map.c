int	*ft_map(int *tab, int length, int (*f)(int))
{
	int	*str;
	int i;

	i = 0;
	str = (int*)malloc(sizeof(*tab) * length);
	while (i < length)
	{
		str[i] = f(tab[i]);
		i++;
	}
	return (str);
}
